# function for delete element from list :-

# del

l = [20, 30, 40, 50, 60]
del l[1]
print(l)

# pop
l = [10, 30, 40, 70]
print(l.pop(2))
print(l)

# remove
l = [78, 56, 45, 34]
l.remove(56)
print(l)

# clear
l = [78, 56, 45, 34]
l.clear()
print(l)

